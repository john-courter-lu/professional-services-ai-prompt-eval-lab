"""Render evaluated results into a Markdown summary report.

Input is the list of evaluated records produced by ``evaluator.evaluate`` — each a dict with
case metadata plus the scoring outcome. Output is a human-readable
``reports/evaluation_summary.md``. No scoring happens here; this module only formats.
"""

from __future__ import annotations

from . import scoring

USE_CASE_ORDER = ["tax", "assurance", "advisory", "wealth"]


def _mean(values):
    return round(sum(values) / len(values), 2) if values else None


def _format_overview(records):
    counts = {"pass": 0, "needs-review": 0, "fail": 0}
    for r in records:
        counts[r["classification"]] += 1
    aggs = [r["aggregate"] for r in records]
    lines = [
        "## Overview",
        "",
        f"- Outputs evaluated: **{len(records)}**",
        f"- Mean aggregate score: **{_mean(aggs)}** (1-5 scale)",
        f"- pass: **{counts['pass']}**  |  needs-review: **{counts['needs-review']}**  "
        f"|  fail: **{counts['fail']}**",
        "",
    ]
    return lines


def _format_use_case_section(use_case, records):
    rows = [r for r in records if r["use_case"] == use_case]
    if not rows:
        return []
    lines = [
        f"### {use_case.capitalize()}",
        "",
        "| Case | Model | Aggregate | Result | Critical flags |",
        "| ---- | ----- | --------- | ------ | -------------- |",
    ]
    for r in sorted(rows, key=lambda x: (x["case_id"], x["model"])):
        flags = "; ".join(r["flags"]) if r["flags"] else "—"
        lines.append(
            f"| {r['case_id']} | {r['model']} | {r['aggregate']:.2f} "
            f"| {r['classification']} | {flags} |"
        )
    lines.append("")
    return lines


def _format_dimension_averages(records):
    lines = [
        "## Average score by dimension",
        "",
        "| Dimension | Weight | Mean score |",
        "| --------- | ------ | ---------- |",
    ]
    for dim in scoring.DIMENSIONS:
        mean = _mean([r["scores"][dim] for r in records])
        lines.append(f"| {dim} | {scoring.WEIGHTS[dim]:.2f} | {mean} |")
    lines.append("")
    return lines


def _format_flagged(records):
    flagged = [r for r in records if r["flags"]]
    lines = ["## Flagged outputs (require human review)", ""]
    if not flagged:
        lines.append("None — no critical flags raised.")
        lines.append("")
        return lines
    for r in sorted(flagged, key=lambda x: x["aggregate"]):
        lines.append(f"- **{r['case_id']} / {r['model']}** ({r['classification']}, "
                     f"aggregate {r['aggregate']:.2f})")
        for reason in r["flags"]:
            lines.append(f"  - {reason}")
    lines.append("")
    return lines


def render_report(records):
    """Return the full report as a Markdown string."""
    lines = [
        "# Evaluation Summary",
        "",
        "_Generated by `python -m src.evaluator evaluate --report`. Synthetic data, QA "
        "demonstration only — not professional advice._",
        "",
    ]

    if not records:
        lines += [
            "## Overview",
            "",
            "No reviewed outputs found under `sample_outputs/reviewed/`. Add scored JSON "
            "files there and re-run to populate this report.",
            "",
        ]
        return "\n".join(lines)

    lines += _format_overview(records)
    lines += ["## Results by use case", ""]
    for use_case in USE_CASE_ORDER:
        lines += _format_use_case_section(use_case, records)
    lines += _format_dimension_averages(records)
    lines += _format_flagged(records)
    return "\n".join(lines)


def write_report(records, out_path):
    """Render and write the report to ``out_path``; return the path written."""
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(render_report(records), encoding="utf-8")
    return out_path
